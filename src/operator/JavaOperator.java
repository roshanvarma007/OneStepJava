package operator;

public class JavaOperator {

	public static void main(String[] args) {
		
		double number1= 14,number2 = 9.1; 
		
		double result = number1+number2;
				
				System.out.println("Plus Result : " + result);
				double result1 = number1-number2;
				
				System.out.println("Minus Result : " + result1);
				
				double result2 = number1*number2;
				
				System.out.println("Multiply Result : " + result2);
				double result3 = number1/number2;
				System.out.println("Divide : " + result3);
				double result4 = number1%number2;
				System.out.println("Reminder : " + result4);
				

	}

}
